        // POST: Producto/Liquidate/5
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Liquidate(int id)
        {
            Producto producto = db.Producto.Find(id);
            if (producto == null)
            {
                return HttpNotFound();
            }
            // Reduce el precio en un 20%
            producto.Precio = (int)(producto.Precio * 0.8); // Resta el 20%

            // Guarda los cambios en la base de datos
            db.Entry(producto).State = EntityState.Modified;
            db.SaveChanges();

            // Redirecciona a la vista de detalles del producto modificado
            return RedirectToAction("Details", new { id = producto.Id });
        }

